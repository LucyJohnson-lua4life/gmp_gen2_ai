const ITEM_ID_TO_SPOT_ID: Map<revmp.Entity, number> = new Map()
let EMPTY_SPOTS: Array<number> = []

export function initWorldItems():void{
    Array.from(ITEM_SPOTS.keys()).forEach( spotId=> {
        const itemId = revmp.addItem(revmp.worlds[0], WeaponInstances.dragonHunterBlade, { amount: 1, position: ITEM_SPOTS.get(spotId)})
        ITEM_ID_TO_SPOT_ID.set(itemId, spotId)
        console.log("item-added at: ", ITEM_SPOTS.get(spotId))
    })
}

export function respawnPlants():void {
    console.log("empty: ", EMPTY_SPOTS.length)
    EMPTY_SPOTS.forEach(spotId => {
        const itemId = revmp.addItem(revmp.worlds[0], WeaponInstances.dragonHunterBlade, { amount: 1, position: ITEM_SPOTS.get(spotId) })
        ITEM_ID_TO_SPOT_ID.set(itemId, spotId)
        console.log("respawned something at pos: ",itemId, ITEM_SPOTS.get(spotId))
    })
    EMPTY_SPOTS = []
}


revmp.on("itemTaken", (entity, item, amount, world, userEvent
) => {
    const spotId = ITEM_ID_TO_SPOT_ID.get(item)
    if (typeof spotId !== 'undefined') {
        EMPTY_SPOTS.push(spotId)
        ITEM_ID_TO_SPOT_ID.delete(item)
    }
});